-- ─────────────────────────────────────────────────────────────────────────────
-- flow_tags (máx 3 por Flow)
-- ─────────────────────────────────────────────────────────────────────────────
create table if not exists public.flow_tags (
  flow_id uuid not null references public.flows(id) on delete cascade,
  tag_id  bigint not null references public.tags(id) on delete cascade,
  primary key (flow_id, tag_id)
);

create or replace function public.enforce_max_tags()
returns trigger language plpgsql as $$
begin
  if (select count(*) from public.flow_tags where flow_id = new.flow_id) >= 3 then
    raise exception 'Un Flow no puede tener más de 3 temas';
  end if;
  return new;
end; $$;
drop trigger if exists trg_max_tags on public.flow_tags;
create trigger trg_max_tags before insert on public.flow_tags
  for each row execute function public.enforce_max_tags();

