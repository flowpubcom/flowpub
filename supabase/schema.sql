-- Mantiene contadores.
create or replace function public.bump_counts()
returns trigger language plpgsql security definer set search_path = public as $$
begin
  if tg_table_name = 'likes' then
    if tg_op = 'INSERT' then
      if new.flow_id is not null then update public.flows set like_count = like_count + 1 where id = new.flow_id; end if;
      if new.comment_id is not null then update public.comments set like_count = like_count + 1 where id = new.comment_id; end if;
    elsif tg_op = 'DELETE' then
      if old.flow_id is not null then update public.flows set like_count = greatest(0, like_count - 1) where id = old.flow_id; end if;
      if old.comment_id is not null then update public.comments set like_count = greatest(0, like_count - 1) where id = old.comment_id; end if;
    end if;
  elsif tg_table_name = 'comments' then
    if tg_op = 'INSERT' then update public.flows set comment_count = comment_count + 1 where id = new.flow_id;
    elsif tg_op = 'DELETE' then update public.flows set comment_count = greatest(0, comment_count - 1) where id = old.flow_id; end if;
  end if;
  return null;
end; $$;

drop trigger if exists trg_like_counts on public.likes;
create trigger trg_like_counts after insert or delete on public.likes
  for each row execute function public.bump_counts();
drop trigger if exists trg_comment_counts on public.comments;
create trigger trg_comment_counts after insert or delete on public.comments
  for each row execute function public.bump_counts();

